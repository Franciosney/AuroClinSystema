public class InserirNafila {
    
    private String ficha;
    private String priocidade;
    private int id_exame;

    public String getFicha() {
        return ficha;
    }

    public void setFicha(String ficha) {
        this.ficha = ficha;
    }

    public String getPriocidade() {
        return priocidade;
    }

    public void setPriocidade(String priocidade) {
        this.priocidade = priocidade;
    }

    public int getId_exame() {
        return id_exame;
    }

    public void setId_exame(int id_exame) {
        this.id_exame = id_exame;
    }

   
    
    
}
