    public void buscarFuncionario(){
    
    
           
         String nome = jTextFieldBuscarFuncionario.getText();
        try {

            FuncionarioDAO daof = new FuncionarioDAO();
            List<Funcionario> listaFuncionario = daof.listarFuncionarioPorNome(nome);

            DefaultTableModel modelo = (DefaultTableModel)jTableFuncionario.getModel();
            modelo.setNumRows(0);

          

            int i = 0;
            for(Funcionario objf: listaFuncionario){
                modelo.addRow(new String[i]);
                jTableFuncionario.setValueAt(objf.getId_funcionario(), i, 0);
                 jTableFuncionario.setValueAt(objf.getNomeFuncionario(), i, 1);
                 jTableFuncionario.setValueAt(objf.getCpfFuncionario(), i, 2);
                 jTableFuncionario.setValueAt(objf.getFuncaoFuncionario(), i, 3);
                
            
                i++;

            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Erro"+e);
        }
       
    
    
    }
    
         
