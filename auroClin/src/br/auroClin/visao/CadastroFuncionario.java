    private void jButtonSalvarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarFuncionarioActionPerformed
        // TODO add your handling code here:
        
        
        
         if (jTextFieldNomeFuncionario.getText().equals("") ) {
            
             JOptionPane.showMessageDialog(null, "Por Favor Preencha o Campo Nome!");
            
        }
         
          else if (jFormattedTextFieldCpf.getText().equals("   .   .   -  ")) {
           
           JOptionPane.showMessageDialog(null, "Preecha o campo CPF");
           
           }
         
         else{
        
             
             String cpf;
                 cpf = jFormattedTextFieldCpf.getText();
                     ValidarCadastroDAO daov = new ValidarCadastroDAO();

        if (daov.validarFuncionario(cpf)) {  
            JOptionPane.showMessageDialog(null, "Usuario Existente no Sistema Insira Outro CPF!"); 
        } else{
             
             
             
             
             
             Funcionario objf = new Funcionario();
            objf.setNomeFuncionario(jTextFieldNomeFuncionario.getText());
            objf.setCpfFuncionario(jFormattedTextFieldCpf.getText());
            String sexo = (String) jComboBoxSexoFuncionario.getSelectedItem();
            objf.setSexoFuncionario(sexo);
           
            objf.setDataNascimento(jFormattedTextFieldDataNascimento.getText());
            objf.setDataCadastro(jFormattedTextFieldDataCadastro.getText());
            String estadoCidade = (String) jComboBoxCidadeEstado.getSelectedItem();
            objf.setCidadeEstado(estadoCidade);
            objf.setBairro(jTextFieldBairro.getText());
       
            objf.setTelefone(jFormattedTextFieldCelular.getText());
        String senha = String.valueOf(jPasswordFieldSenhaFuncionario.getPassword());
            objf.setSenhaFuncionario(senha);
            String categoria = (String) jComboBoxFuncao.getSelectedItem();
            objf.setFuncaoFuncionario(categoria);
            objf.setRua(jTextFieldRua.getText());
         
         
            
            
            
    
           
            
            try {
                
                FuncionarioDAO daof = new FuncionarioDAO();
                daof.cadastrarFuncionario(objf);
                
                 
            
            JOptionPane.showMessageDialog(null, "Funcionário Cadastrado com Sucesso!");
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(null, "Erro ao Cadastrar Funcionário!"+e);
            }
            
            
            
            jTextFieldNomeFuncionario.setText(""); 
            jTextFieldBairro.setText("");
            jFormattedTextFieldCpf.setText("");
            jPasswordFieldSenhaFuncionario.setText("");
            jFormattedTextFieldDataNascimento.setText("");
            jFormattedTextFieldDataCadastro.setText("");
            jFormattedTextFieldCelular.setText("");
            jTextFieldRua.setText("");
            jTextFieldRgFuncionario.setText("");
           
        
        
        
        
        }
        
        
        
         }
        
        
        
        
        
        
    }//GEN-LAST:event_jButtonSalvarFuncionarioActionPerformed

