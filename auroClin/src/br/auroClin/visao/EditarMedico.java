    public void buscarFuncionaios(){
    
       String nome = jTextFieldBuscarfuncionarioNome.getText();
 
     String cpf = jTextFieldBuscarCpf.getText();
           
        try {

            FuncionariosDAO daoe = new FuncionariosDAO();
            List<Medico> listaEnfermeiro = daoe.listarMedicos(nome,cpf);

            DefaultTableModel modelo = (DefaultTableModel)jTableEnfermeiro.getModel();
            modelo.setNumRows(0);

          

            int i = 0;
            for(Medico objf: listaEnfermeiro){
                modelo.addRow(new String[i]);
                jTableEnfermeiro.setValueAt(objf.getId_medico(), i, 0);
                 jTableEnfermeiro.setValueAt(objf.getNomeMedico(), i, 1);
                 jTableEnfermeiro.setValueAt(objf.getCpf(), i, 2);
                 jTableEnfermeiro.setValueAt(objf.getEspecializacao(), i, 3);
                
            
                i++;

            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Erro"+e);
        }
       

    
    
    
    }
    
    
