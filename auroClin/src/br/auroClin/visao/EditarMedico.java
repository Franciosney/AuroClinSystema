    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:

        if (jTextFieldIdPaciente.getText().equals("")) {

            JOptionPane.showMessageDialog(rootPane, "Selecione um Funcionario na Tabela Acima");

        }else{

            String nome = jTextFieldNomePaciente.getText();
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "Você deseja alterar os dados de " +nome +" ?", "Title on Box", dialogButton);
            if(dialogResult == 0) {

                FuncionariosDAO daof = new FuncionariosDAO();
                Medico objm = new Medico();
                objm.setNomeMedico(jTextFieldNomePaciente.getText());
                objm.setCrm(jTextFieldcrm.getText());
                objm.setCpf(jFormattedTextFieldCpf.getText());
                   String senha = String.valueOf(jPasswordFieldSenhaFuncionario.getPassword());
                objm.setSenha(senha);
                 objm.setEspecializacao(jTextFieldFuncao.getText());
                 objm.setSexo(jTextFieldSexoVindo.getText());
                 objm.setDataNascimento(jFormattedTextFieldDataNascimento.getText());
                 objm.setDataCadastro(jFormattedTextFieldDataCadastro.getText());
                 objm.setCidadeEstado(jTextFieldCidadeVinda.getText());
                  objm.setBairro(jTextFieldBairro.getText());
                   objm.setRua(jTextFieldRua.getText());
                    objm.setEmail(jTextFieldEmail.getText());
                    objm.setId_medico(Integer.parseInt(jTextFieldIdPaciente.getText()));
                 
                 
             
                daof.editarMedico(objm);

                JOptionPane.showMessageDialog(rootPane, "Dados Alterados Com Sucesso!");

                jTextFieldNomePaciente.setText("");
                //jTextFieldRgPaciente.setText("");
                jFormattedTextFieldCpf.setText("");
                // op.setRg(jTextFieldNomePaciente.getText());
                jTextFieldSexoVindo.setText("");
                jFormattedTextFieldDataNascimento.setText("");
                jFormattedTextFieldDataCadastro.setText("");
                //op.setPlano(jTextFieldNomePaciente.getText());
                jTextFieldCidadeVinda.setText("");
                jTextFieldBairro.setText("");
                jTextFieldRua.setText("");

                jTextFieldIdPaciente.setText("");

            }else{

                JOptionPane.showMessageDialog(rootPane, "Dados Não Alterados");

                jTextFieldNomePaciente.setText("");

                jFormattedTextFieldCpf.setText("");
                // op.setRg(jTextFieldNomePaciente.getText());
                jTextFieldSexoVindo.setText("");
                jFormattedTextFieldDataNascimento.setText("");
                jFormattedTextFieldDataCadastro.setText("");
                //op.setPlano(jTextFieldNomePaciente.getText());
                jTextFieldCidadeVinda.setText("");
                jTextFieldBairro.setText("");
                jTextFieldRua.setText("");

                

                jTextFieldIdPaciente.setText("");

            }

        }
    }//GEN-LAST:event_jButton11ActionPerformed

