    private void jTableEnfermeiroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEnfermeiroMouseClicked
        // TODO add your handling code here:

        int id_medico = Integer.parseInt(jTableEnfermeiro.getValueAt(jTableEnfermeiro.getSelectedRow(), 0).toString());

        try {

            FuncionariosDAO daop = new FuncionariosDAO();
            List<Medico> listaPaciente = daop.listarMedicosPorId(id_medico);

            for(Medico objp: listaPaciente){
                jTextFieldIdPaciente.setText(Integer.toString(objp.getId_medico()));
                jTextFieldNomePaciente.setText(objp.getNomeMedico());
                jFormattedTextFieldCpf.setText(objp.getCpf());
                jTextFieldcrm.setText(objp.getCrm());
                jPasswordFieldSenhaFuncionario.setText(objp.getSenha());
                jTextFieldFuncao.setText(objp.getEspecializacao());
                jTextFieldSexoVindo.setText(objp.getSexo());
                jFormattedTextFieldDataNascimento.setText(objp.getDataNascimento());
                jFormattedTextFieldDataCadastro.setText(objp.getDataCadastro());
                jTextFieldCidadeVinda.setText(objp.getCidadeEstado());
                jTextFieldBairro.setText(objp.getBairro());
                jTextFieldRua.setText(objp.getRua());
                jTextFieldEmail.setText(objp.getEmail());
                

                //System.out.println(objp.getPlano());

            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Erro"+e);
        }

    }//GEN-LAST:event_jTableEnfermeiroMouseClicked

