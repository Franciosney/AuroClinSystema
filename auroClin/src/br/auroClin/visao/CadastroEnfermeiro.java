    public void buscarEnfermeiro(){
    
    
        
         String nome = jTextFieldBuscarEnfermeiro.getText();
           String buscar01 = "Enfermeiro";
            String buscar02 = "Técnico de Enfermagem";
        try {

            EnfermeiroDAO daoe = new EnfermeiroDAO();
            List<Enfermeiro> listaEnfermeiro = daoe.listarFuncionarioPorNome(nome, buscar01, buscar02);

            DefaultTableModel modelo = (DefaultTableModel)jTableEnfermeiro.getModel();
            modelo.setNumRows(0);

          

            int i = 0;
            for(Enfermeiro objf: listaEnfermeiro){
                modelo.addRow(new String[i]);
                jTableEnfermeiro.setValueAt(objf.getId_enfermeiro(), i, 0);
                 jTableEnfermeiro.setValueAt(objf.getNomeEnfermeiro(), i, 1);
                 jTableEnfermeiro.setValueAt(objf.getCpf(), i, 2);
                 jTableEnfermeiro.setValueAt(objf.getFuncao(), i, 3);
                
            
                i++;

            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Erro"+e);
        }
        
        
        
    
    
    
    }
    
    
         
