    private void jButtonSalvarEnfermeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarEnfermeiroActionPerformed
        // TODO add your handling code here:
        
         
        if (jTextFieldNomeEnfermeiro.getText().equals("")) {
            
             JOptionPane.showMessageDialog(null, "Por Favor Preencha o Campo Nome!");
            
             
        } 
        
         else if (jFormattedTextFieldCpf.getText().equals("   .   .   -  ")) {
           
           JOptionPane.showMessageDialog(null, "Preecha o campo CPF");
           
           }
        
         
         
        else{
             
             
             String cpf;
         
            cpf = jFormattedTextFieldCpf.getText();
                     ValidarCadastroDAO daov = new ValidarCadastroDAO();

        if (daov.validarEnfermeiro(cpf)) {  
            JOptionPane.showMessageDialog(null, "Usuario Existente no Sistema Insira Outro CPF!"); 
             jTextFieldNomeEnfermeiro.setText("");
            jTextFieldBairro.setText("");
            jPasswordFieldSenhaEnfermeiro.setText("");
            jFormattedTextFieldDataNascimento.setText("");
            jFormattedTextFieldDataCadastro.setText("");
            jFormattedTextFieldCelular.setText("");
            jTextFieldRua.setText("");
            jFormattedTextFieldCpf.setText("");
        }else{
        
            Enfermeiro obje = new Enfermeiro();
            obje.setNomeEnfermeiro(jTextFieldNomeEnfermeiro.getText());
            obje.setCpf(jFormattedTextFieldCpf.getText());
            String sexo = (String) jComboBoxSexoPaciente.getSelectedItem();
            obje.setSexo(sexo);
            obje.setEspecializacao("");
            obje.setDataNascimento(jFormattedTextFieldDataNascimento.getText());
            obje.setDataCadastro(jFormattedTextFieldDataCadastro.getText());
            String estadoCidade = (String) jComboBoxCidade.getSelectedItem();
            obje.setCidadeEstado(estadoCidade);
            obje.setBairro(jTextFieldBairro.getText());
            obje.setRua(jTextFieldRua.getText());
            obje.setTelefone(jFormattedTextFieldCelular.getText());
        String senha = String.valueOf(jPasswordFieldSenhaEnfermeiro.getPassword());
            obje.setSenha(senha);
            String categoria = (String) jComboBoxFuncao.getSelectedItem();
            obje.setFuncao(categoria);
            obje.setEmail("");
            obje.setCrm("");
         
            
            
            
    
           
            
            try {
                
                EnfermeiroDAO daoe = new EnfermeiroDAO();
                daoe.cadastrarEnfermeiro(obje);
                
      
            
            JOptionPane.showMessageDialog(null, "Enfermeiro Cadastrado com Sucesso!");
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(null, "Erro ao Cadastrar Enfermeiro!"+e);
            }
            
            
          
             jTextFieldNomeEnfermeiro.setText("");
            jTextFieldBairro.setText("");
            jPasswordFieldSenhaEnfermeiro.setText("");
            jFormattedTextFieldDataNascimento.setText("");
            jFormattedTextFieldDataCadastro.setText("");
            jFormattedTextFieldCelular.setText("");
            jTextFieldRua.setText("");
            jFormattedTextFieldCpf.setText("");
        
        
        
        }
        
        }
        
        
    }//GEN-LAST:event_jButtonSalvarEnfermeiroActionPerformed

