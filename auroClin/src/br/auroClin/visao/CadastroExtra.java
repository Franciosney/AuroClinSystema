      public void listarCidade(){
    
        try {
            
              FuncaoSistemaDAO daocos = new FuncaoSistemaDAO();
            List<Cidades> listaConsulta = daocos.listarCidades();
            
            DefaultTableModel modelo = (DefaultTableModel)jTableCidades.getModel();
            modelo.setNumRows(0);
            
            int i = 0;
            for(Cidades cons: listaConsulta){
modelo.addRow(new String[i]);
jTableCidades.setValueAt(cons.getId_cidade(), i, 0);
jTableCidades.setValueAt(cons.getNomeCidade(), i, 1);

            i++;
            }
            
        } catch (Exception e) {}
        }
    
    
    
      
