    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         String cidade = jTextFieldCidade.getText();
         String estado = jTextFieldEstado.getText();
        
         String traco = " - ";
         
         String fina = cidade+traco+estado;
          FuncaoSistemaDAO daof = new FuncaoSistemaDAO();
         
      
        if (jTextFieldCidade.getText().equals("")) {
            
            JOptionPane.showMessageDialog(rootPane, "Adicione uma Cidade!");
            
            
        } else if(jTextFieldEstado.getText().equals("")){
        
        
         JOptionPane.showMessageDialog(rootPane, "Adicione um Estado!");
        
        }else{
        
        
        
        if (daof.validarCidade(fina)) {
            
JOptionPane.showMessageDialog(rootPane, "Cidade Existente no Sistema!");
            
        }else{
        
        daof.cadastrarCidade(fina);
        JOptionPane.showMessageDialog(rootPane, "Cidade Cadastrada Com Sucesso");
        
        }

        
        
        
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

